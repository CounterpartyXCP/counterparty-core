# permutation function
# p(a^i) = a^(2-i)
# p(ba^i) = ba^i

p = [4, 1, 2, 3, 0, 5, 24, 7, 22, 9, 20, 11, 18, 13, 16, 15, 14, 17, 12, 19, 10, 21, 8, 23, 6, 25]

def p_k(x, k):
    y = x
    for r in range(k):
        y = p[y]
    return y

def verify(x):
    x = [ord(c)-65 for c in x]
    for v in x:
        assert(v >= 0 and v < 26)
    val = 0
    for k in range(len(x)):
        val = M[val][p_k(x[k],k+1)]
    return val == 0

def compute(x):
    x = [ord(c)-65 for c in x]
    for v in x:
        assert(v >= 0 and v < 26)
    val = 0
    for k in range(len(x)):
        val = M[val][p_k(x[k],k+1)]
    for k in range(len(M)):
        el = p_k(k,len(x)+1) 
        if M[val][el] == 0:
            return chr(k+65)
    assert(False)
